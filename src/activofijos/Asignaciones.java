        String cSo = "";
        String cVersion = "";
        String cArquitectura = "";
        String cColor = "";
        String cCajachica = "";
        String cTempo = "";

        cCodigoActivo = jTextcodigoactivo.getText();
        cDescripcion = jTextdescripcion.getText();
        cMarca = jTextmarca.getText();
        cModelo = jTextmodelo.getText();
        cSerie = jTextserie.getText();
