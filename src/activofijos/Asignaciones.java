    private Integer devolverCodigo() throws SQLException {
        Integer nDevolver = 0;
        String lcSQL = "SELECT max(codigoactivo) codigoactivo FROM activos;";

        Conectar nConexion = new Conectar();
        nConexion.conectar();
        Statement st = nConexion.conexion();
        
        ResultSet rs = nConexion.slqConsulta(st, lcSQL);
        
        rs.next();
        nDevolver = (Integer) rs.getObject(1) ;
        nDevolver++;
        
        nConexion.cerrar(st);
        nConexion.desconectar();

        return nDevolver;
    }
    private void jComboBoxcodigoubicacionIniciar(String pBuscar) {
        // llenar el combo box con los datos requeridos
       jComboBoxcodigoubicacion.removeAllItems();
       
       String cWhere = pBuscar;
       String cWhere1 = pBuscar;
       String cSql;
       if(cWhere.length() > 0){
           cWhere = " WHERE codigoubicacion = " + cWhere + "\n";
           cWhere1 = " WHERE codigoubicacion != " + cWhere1 + "\n";
       }
       
        cSql = "SELECT (codigoubicacion  || Repeat(' ',7) || descripcion) As ubicaciones, '*' AS orden \n" +
                " FROM ubicaciones \n" + 
                cWhere + "UNION \n" +
                "SELECT (codigoubicacion  || Repeat(' ',7) || descripcion) As ubicaciones, '' AS orden \n" +
                " FROM ubicaciones \n" + 
                cWhere1 +
                " ORDER BY 2 DESC, 1 \n" ;
        
        //System.out.println(cSql);
        
        Conectar nConexion = new Conectar();
        nConexion.conectar();
        Statement st = nConexion.conexion();
        ResultSet rs = nConexion.slqConsulta(st, cSql);  // Resultado
       
        String cPrimera = "";
        try {
            while (rs.next()) { 
                if(cPrimera.length() == 0) {
                    cPrimera = rs.getString("ubicaciones");
                    
                }
                
                        
                String cLinea = rs.getString("ubicaciones");
                // aqui recuperamos los valores obtenidos y se los pasamos al jComboBix
                this.jComboBoxcodigoubicacion.addItem(cLinea);
            } // fin de while
        } catch (Exception eEc ){
            // Mostrr el error
            System.out.println("Error Sql " + eEc);
        }
        this.jComboBoxcodigoubicacion.setSelectedItem(cPrimera);
        nConexion.cerrar(st);
        nConexion.desconectar();
    } // fin de JComboBoxcodigoubicacionIniciar()
        
