    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String cResponsable_id = "0";
        int V = Integer.parseInt(cResponsable_id); // para convertir string a entero
        String cNombre = "";
        String [] Datos = new String [2];

        cResponsable_id = jTextResponsable.getText();
        cNombre = jTextNombre.getText();

        Datos [0] = cResponsable_id;
        Datos [1] = cNombre;
        jTextResponsable.setText(null);
        jTextNombre.setText(null);
        modelo.addRow(Datos);

        // Codigo para conectarse y grabar en la base de datos
        Conectar nConexion = new Conectar();
        nConexion.conectar();
        Statement st = nConexion.conexion();

        String cSql;
        cSql = "INSERT INTO responsables" +
        " SELECT '" + cResponsable_id + "',  '" + cNombre + "' " +
        " WHERE NOT EXISTS " +
        "    ( SELECT * FROM responsables " +
        "         WHERE codigoresponsable = '" + cResponsable_id + "'); ";

        nConexion.slqActualiza(st, cSql);
        nConexion.cerrar(st);
        nConexion.desconectar();
        
    }//GEN-LAST:event_jButton2ActionPerformed

